package negocio.bicicleta.imp;

public class TransferBicicleta 
{
	protected int id;
	protected String material;
	protected String marca;
	protected double precio;
	protected boolean activo;

	public TransferBicicleta(String material, String marca, double precio, boolean activo) 
	{
		this.material = material;
		this.marca = marca;
		this.precio = precio;
		this.activo = activo;
	}

	public TransferBicicleta(int id, String material, String marca,	double precio, boolean activo)
	{
		this.id = id;
		this.material = material;
		this.marca = marca;
		this.precio = precio;
		this.activo = activo;
	}

	public int getId() 
	{
		return id;
	}

	public String getMaterial() 
	{
		return material;
	}

	public String getMarca() 
	{
		return marca;
	}

	public double getPrecio() 
	{
		return precio;
	}

	public boolean getActivo() 
	{
		return activo;
	}

	public void setMaterial(String material) 
	{
		this.material = material;
	}

	public void setMarca(String marca)
	{
		this.marca = marca;
	}

	public void setPrecio(double precio) 
	{
		this.precio = precio;
	}

	public void setActivo(boolean activo) 
	{
		this.activo = activo;
	}
}